num_float = 9.99

# float to int
num_int = int(num_float)

print(num_int)
print(type(num_int)) 


# int to float
num_int = 7

num_float = float(num_int)

print(num_float)
print(type(num_float))

# list to tuple:

my_list = [1, 2, 3]

my_tuple = tuple(my_list)

print(my_tuple)
print(type(my_tuple))

# tuple to list:

my_tuple = (1, 2, 3)

my_list = list(my_tuple)

print(my_list)
print(type(my_list))

# string to list:

my_string = "hello"

my_list = list(my_string)

print(my_list)
print(type(my_list))

# list to set:

my_list = [1, 2, 3, 4, 5]

my_set = set(my_list)

print(my_set)
print(type(my_set))